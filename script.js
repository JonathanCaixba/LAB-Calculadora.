// Funciones
function division (a, b){
    let total = a / b;
    return total;
}

function multiplicacion (a, b){
    let total = a * b;
    return total;
}

function sumar (a, b){
    let total = a + b;
    return total;
}

function resta (a, b){
    let total = a - b;
    return total;
}

// Proceso
let resultadoDivi = division(10, 5);
console.log("Esta es la division:", resultadoDivi);

let resultadoMulti = multiplicacion(10, 5);
console.log("Esta es la multiplicacion:", resultadoMulti);

let resultadoSuma = sumar(10, 5);
console.log("Esta es la suma:", resultadoSuma);

let resultadoRest = resta(10, 5);
console.log("Esta es la resta:", resultadoRest);

